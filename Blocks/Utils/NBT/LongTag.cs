namespace Blocks.Utils.NBT
{
	public class LongTag : Tag
	{
		public long Value;
		
		public LongTag(long value)
		{
			Name = "LongTag";
			
			Value = value;
		}
		
		public byte Id
		{
			get { return TAG_Long; }
		}
	}
}
