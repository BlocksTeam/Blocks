using System;

using Blocks.Utils;

namespace Blocks.Level.Blocks
{
	/// <summary>
	/// Block of Minecraft World.
	/// </summary>
	public abstract class Block : MetaTag
	{
		public int Id
		{
			protected set;
			get;
		}
		
		public string Name
		{
			protected set;
			get;
		}
		
		public override string ToString()
		{
			return Name;
		}
	}
}
