using System;

using Blocks.Utils;

namespace Blocks.Player
{
	/// <summary>
	/// Abstract Class of Game Player.
	/// </summary>
	public abstract class Player : MetaTag
	{
		private string PlayerName = "";
		
		public string Name
		{
			get { return Name; }
		}
		
		public override string ToString()
		{
			return PlayerName;
		}

		
		public string PlayerLabel, NameTag;
	}
}
